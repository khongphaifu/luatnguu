namespace mnp
{
    
    bool cmp(bg a,bg b)
    {
        if(a.x==b.x) return a.id<b.id;
        return a.x<b.x;
    }
    
    void sol()
    {
        sort(all(v),cmp);
        stack<ll> st;
        ll ans=0;
        for(auto it:v)
        {
            if(it.id==-1)
            {
                st.push(it.w);
            }
            else if(it.id==1 && a[it.w]>0)
            {
                stack<ll> st1;
                ll x=a[it.w];
                ans=ans+x;
                while(!st.empty())
                {
                    a[st.top()]-=min(x,max(it.x-f[st.top()]+1,0ll));
                    f[st.top()]=f[st.top()]+min(x,max(it.x-f[st.top()]+1,0ll));
                    if(a[st.top()]>0)
                    {
                        st1.push(st.top());
                    }
                    st.pop();
                }
                while(!st1.empty())
                {
                    st.push(st1.top());
                    st1.pop();
                }
            }
        }
        cout<<ans;
    }
}

    

