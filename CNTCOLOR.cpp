    void dfs(ll u,ll pre)
    {
        for(auto it:g[u])
        {
            if(it==pre) continue;
            dfs(it,u);
            if(s[it].size()>s[u].size()) swap(s[it],s[u]);
            for(auto it:s[it]) s[u].insert(it);
        }
        f[u]=s[u].size();
    }

