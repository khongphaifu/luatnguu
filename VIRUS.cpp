#define TASK  "4"
#include <bits/stdc++.h> 
#define             pb  push_back  
#define             ll  long long
#define             se  second 
#define             fi  first
#define             el  "\n"
#define      alla(a,n)  a+1,a+n+1
#define         all(v)  v.begin(),v.end()
#define      fu(i,a,b)  for(ll i=a;i<=b;i++)
#define     fud(i,a,b)  for(ll i=a;i>=b;i--)
using namespace std;

const long long inf = 1e18;
const int N = 1e5 + 5 , N1=1e3 +5 ,  MOD = 1e9 + 7 , LG=31;

ll n,root1,root2,d,ans;
ll a[N],b[N],h[N],f[N],bitch[N],DQN[N];
vector<ll> g1[N],g2[N];

void up(ll x,ll v)
{
    for(;x<=n;x+= x & -x) bitch[x]+=v;
}

ll get(ll x)
{
    ll res=0;
    for(;x>=1;x &=x-1) res+=bitch[x];
    return res;
}

void dfs2(ll u,ll pre)
{
    d++;
    f[u]=1;
    h[u]=d;
    for(auto it:g2[u])
    {
        if(it==pre) continue;
        dfs2(it,u);
        f[u]+=f[it];
    }
}

void dfs1(ll u,ll pre)
{
    ans+=get(h[u]);
    up(h[u],1);up(h[u]+f[u],-1);
    for(auto it:g1[u])
    {
        if(it==pre) continue;
        DQN[it]=DQN[u]+1;
        dfs1(it,u);
    }
    up(h[u],-1);up(h[u]+f[u],1);
}

