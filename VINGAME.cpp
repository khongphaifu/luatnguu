using namespace std;

const long long inf = 1e18;
const int N = 1e6 + 5 , N1=1e3 +5 ,  MOD = 1e9 + 7 , LG=31;


ll n,m,scc,stt;
ll a[N],num[N],low[N],del[N],var[N],f[N];
vector<ll> g[N],gr[N];
stack<ll> s;
queue<ll> ds;

void dfs(ll u,ll pre)
{
    num[u]=low[u]=++stt;
    s.push(u);
    for(auto it:g[u])
    {
        if(del[it]) continue;
        if(!num[it])
        {
            dfs(it,u);
            low[u]=min(low[it],low[u]);
        }
        else low[u]=min(low[u],num[it]);
    }

    if(num[u]==low[u])
    {
        ll v;
        scc++;
        do
        {
            v=s.top();
            s.pop();
            del[v]=scc;
        }
        while(u!=v);
    }
}

